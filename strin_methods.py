course = "python programming"
print(course.upper())   # upper Case method
print(course.lower())   # lower case
print(course.title())   # fist letter of each word capitalized
print(course.strip())   # remove white space from beginning of string
print(course.find("pro"))    # find index
print(course.replace("p", "j"))     # replace p with j
print("pro" in course)  # True or False
print("swift" not in course)
print(course)           # original string not affected
