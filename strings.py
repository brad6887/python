course = "Python Programming"
print(len(course))      # number of characters
print(course[0])        # show first character
print(course[-1])       # first character from end of string
print(course[0:3])      # first 3 characters from string
print(course[0:])       # Show string from beginning to end
print(course[:3])       # Show string from beginning to 3
print(course[:])        # Show entire string
